package a02_Ausgabe;



public class Geschwindigkeit {

	public static void main(String[] args) {
		
		double s = 50;
		double t = 3;
		
		System.out.println("Die Geschwindigkeit wird berechnet mit v = s/t. \n");
		System.out.println("Wenn gilt: \t" + "s = 50m\t\t\n" + "\t\tt = 3s \n");
		System.out.print("Ergibt sich: ");
		System.out.println("\tv="+(s/t)+"m/s");
		
	}

	
}
